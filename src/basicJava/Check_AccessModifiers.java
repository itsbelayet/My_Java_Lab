package basicJava;

public class Check_AccessModifiers {
    public static void publicAccessModi(){
        System.out.println("This is Public access Modifiers");
    }
    private void privateAccessModi(){
        System.out.println("This is Private access Modifiers");
    }
    protected void protectedAccessModi(){
        System.out.println("This is Protected access Modifiers");
    }
    public static void defaultAccessModi(){
        System.out.println("This is Default access Modifiers");
    }
}
